// Java Program to Check if Given Integer is Odd or Even

import java.io.*;
import java.util.Scanner;

class Even_or_Odd {

    public static void main(String[] args)
    {
        int num = 10;
        if (num % 2 == 0) {

            System.out.println("Entered Number is Even");
        }

        else {
            
            System.out.println("Entered Number is Odd");
        }
    }
}